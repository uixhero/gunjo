#!/usr/bin/env node

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import {
  collectExtendedTokenUsage,
  escapeCell,
  listSourceFiles,
  needsExtendedTokenReview,
} from "./audit-extended-token-utils.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_PATH = join(ROOT, "docs/runtime-extended-token-audit.md");
const AUDIT_ISSUE = 269;

const TARGET_PATHS = [
  "src/components/inputs",
  "src/components/display",
  "src/components/feedback",
  "src/components/navigation",
  "src/components/overlay",
  "src/components/layout",
  "src/components/patterns",
  "app/components",
  "app/lib",
  "app/patterns/_lib",
  "app/patterns/auth/_lib",
  "app/patterns/dashboard/_lib",
  "app/patterns/media-library/_lib",
];

function auditFiles() {
  return [
    ...new Set(
      TARGET_PATHS.flatMap((targetPath) => listSourceFiles(join(ROOT, targetPath)))
    ),
  ]
    .sort((a, b) => a.localeCompare(b))
    .filter((filePath) => existsSync(filePath))
    .map((filePath) => ({
      file: relative(ROOT, filePath),
      usage: collectExtendedTokenUsage(readFileSync(filePath, "utf-8")),
    }));
}

function countWith(results, key) {
  return results.filter((item) => item.usage[key].length > 0).length;
}

function hasInlineImplementationStyles(item) {
  return (
    item.usage.inlineMotion.length > 0 ||
    item.usage.inlineLayering.length > 0 ||
    item.usage.inlineOpacity.length > 0 ||
    item.usage.inlineBorderWidth.length > 0
  );
}

function buildMarkdown(results) {
  const generated = new Date().toISOString().slice(0, 10);
  const lines = [
    "# Runtime Extended Token Audit",
    "",
    `Generated: ${generated}`,
    "",
    `Parent issue: #${AUDIT_ISSUE}`,
    "",
    "This report is generated by `npm run docs:audit:runtime-extended-tokens`.",
    "",
    "Scope: shared runtime/demo helpers under `src/components/**`, `app/components/**`, `app/lib/**`, and pattern `_lib` folders.",
    "",
    "## Summary",
    "",
    `- Runtime files: ${results.length}`,
    `- Files with arbitrary motion classes: ${countWith(results, "motionArbitrary")}`,
    `- Files with arbitrary layering classes: ${countWith(results, "layeringArbitrary")}`,
    `- Files with arbitrary opacity classes: ${countWith(results, "opacityArbitrary")}`,
    `- Files with arbitrary border width classes: ${countWith(results, "borderWidthArbitrary")}`,
    `- Files with arbitrary container sizing classes: ${countWith(results, "containerSizingArbitrary")}`,
    `- Files with arbitrary density classes: ${countWith(results, "densityArbitrary")}`,
    `- Files with inline implementation styles: ${results.filter(hasInlineImplementationStyles).length}`,
    `- Files needing review: ${results.filter(needsExtendedTokenReview).length}`,
    "",
    "## Matrix",
    "",
    "| File | Motion | Motion arbitrary | Layering | Layering arbitrary | Overlay position | Opacity | Opacity arbitrary | Border width | Border width arbitrary | Breakpoints | Container sizing | Container sizing arbitrary | Icon size | Density | Density arbitrary | Inline implementation styles | Inline container sizing |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
  ];

  for (const item of results) {
    const inlineReview = [
      ...item.usage.inlineMotion,
      ...item.usage.inlineLayering,
      ...item.usage.inlineOpacity,
      ...item.usage.inlineBorderWidth,
    ];
    lines.push(
      `| ${escapeCell(item.file)} | ${escapeCell(item.usage.motion)} | ${escapeCell(item.usage.motionArbitrary)} | ${escapeCell(item.usage.layering)} | ${escapeCell(item.usage.layeringArbitrary)} | ${escapeCell(item.usage.overlayPosition)} | ${escapeCell(item.usage.opacity)} | ${escapeCell(item.usage.opacityArbitrary)} | ${escapeCell(item.usage.borderWidth)} | ${escapeCell(item.usage.borderWidthArbitrary)} | ${escapeCell(item.usage.breakpoints)} | ${escapeCell(item.usage.containerSizing)} | ${escapeCell(item.usage.containerSizingArbitrary)} | ${escapeCell(item.usage.iconSize)} | ${escapeCell(item.usage.density)} | ${escapeCell(item.usage.densityArbitrary)} | ${escapeCell(inlineReview)} | ${escapeCell(item.usage.inlineContainerSizing)} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- Arbitrary classes and inline motion/border width styles should be reviewed before replacement.",
    "- Inline layering and opacity values are listed for visibility because charts, SVG layers, overlays, and clipboard fallbacks can require implementation-level values.",
    "- Inline container sizing is listed separately because many charts and frames require measured dimensions."
  );

  return `${lines.join("\n")}\n`;
}

function main() {
  const results = auditFiles();
  writeFileSync(OUTPUT_PATH, buildMarkdown(results), "utf-8");
  console.log(`runtime-extended-token-audit: wrote ${relative(ROOT, OUTPUT_PATH)}`);
}

main();
