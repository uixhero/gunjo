#!/usr/bin/env node

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import puppeteer from "puppeteer";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_MD = join(ROOT, "docs", "en-locale-sweep-audit.md");
const OUTPUT_JSON = join(ROOT, "docs", "en-locale-sweep-audit.json");
const DEFAULT_BASE_URL = "http://127.0.0.1:13030";
const JAPANESE_TEXT_PATTERN = /[\u3040-\u30ff\u3400-\u9fff]/;

const DEFAULT_ROUTES = [
  { route: "/", label: "Home" },
  { route: "/showcase", label: "Showcase" },
  { route: "/docs/introduction", label: "Docs introduction" },
  { route: "/docs/components/button", label: "Button docs" },
  { route: "/docs/components/status-screen", label: "StatusScreen docs" },
  { route: "/docs/colors", label: "Colors" },
  { route: "/docs/tokens", label: "Tokens" },
  { route: "/docs/guidelines/docs-standards", label: "Docs standards" },
  { route: "/docs/ai-handoff", label: "AI handoff" },
  { route: "/patterns", label: "Patterns index" },
  { route: "/patterns/dashboard/overview", label: "Dashboard pattern" },
  { route: "/patterns/auth/login", label: "Auth pattern" },
  { route: "/patterns/media-library", label: "Media library pattern" },
  { route: "/patterns/not-found", label: "Not found pattern" },
  { route: "/missing-public-readiness-page", label: "App 404", expectedStatus: 404 },
];

const VIEWPORTS = {
  desktop: { width: 1440, height: 1000, deviceScaleFactor: 1 },
  mobile: { width: 390, height: 844, deviceScaleFactor: 2, isMobile: true, hasTouch: true },
};

const THEMES = ["light", "dark"];

function delay(ms) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms);
  });
}

function getArg(name, fallback = null) {
  const prefix = `--${name}=`;
  const found = process.argv.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : fallback;
}

function parseCsv(value) {
  return String(value ?? "")
    .split(",")
    .map((item) => item.trim())
    .filter(Boolean);
}

function parseOptions() {
  const routesArg = getArg("routes");
  const viewportsArg = getArg("viewports");
  const themesArg = getArg("themes");
  return {
    baseUrl: getArg("base-url", DEFAULT_BASE_URL),
    reportPath: getArg("report", OUTPUT_MD),
    jsonPath: getArg("json", OUTPUT_JSON),
    timeoutMs: Number(getArg("timeout", "45000")),
    settleMs: Number(getArg("settle", "500")),
    routes: routesArg
      ? parseCsv(routesArg).map((route) => ({ route, label: route }))
      : DEFAULT_ROUTES,
    viewports: viewportsArg
      ? parseCsv(viewportsArg).filter((name) => name in VIEWPORTS)
      : Object.keys(VIEWPORTS),
    themes: themesArg
      ? parseCsv(themesArg).filter((name) => THEMES.includes(name))
      : THEMES,
  };
}

function escapeCell(value) {
  const text = String(value ?? "");
  return text ? text.replace(/\|/g, "\\|").replace(/\n/g, "<br>") : "-";
}

function shortText(value, max = 160) {
  const text = String(value ?? "").replace(/\s+/g, " ").trim();
  if (text.length <= max) return text;
  return `${text.slice(0, max - 1)}…`;
}

function isExpectedJapaneseCandidate(route, text, selector) {
  if (route === "/" && /群青|媚茶|Gunjō|Kobicha/.test(text)) return true;
  if (route === "/showcase" && selector.includes("span > span > span")) return true;
  if (
    route.startsWith("/docs/") &&
    (selector === "div > h4 > a > span" ||
      selector === "div > div > a > span" ||
      selector === "div > nav > a > span" ||
      selector === "div > nav > button > span" ||
      selector === "div > div > h1 > span" ||
      selector === "div > div > span > span")
  ) {
    return true;
  }
  return false;
}

function isExpectedFailedRequest(routeItem, request) {
  if (request.url.includes("/_next/static/chunks/") && request.failure.includes("ERR_ABORTED")) {
    return true;
  }
  if (request.url.includes("/embed/") && request.failure.includes("ERR_ABORTED")) {
    return true;
  }
  if (routeItem.expectedStatus === 404 && request.failure === "HTTP 404") {
    try {
      return new URL(request.url).pathname === routeItem.route;
    } catch {
      return request.url.includes(routeItem.route);
    }
  }
  return false;
}

function isExpectedConsoleError(routeItem, message) {
  if (routeItem.expectedStatus === 404 && message.includes("404")) return true;
  return false;
}

function buildMarkdown(report) {
  const lines = [
    "# EN Locale Sweep Audit",
    "",
    `Generated: ${report.generatedAt}`,
    "",
    "Parent issue: #282",
    "",
    "This report is generated by `npm run docs:audit:en-locale-sweep`.",
    "",
    "## Scope",
    "",
    `- Base URL: ${report.baseUrl}`,
    `- Routes: ${report.routes.length}`,
    `- Viewports: ${report.viewports.join(", ")}`,
    `- Themes: ${report.themes.join(", ")}`,
    "- Locale is forced with `?locale=en` and localStorage.",
    "",
    "## Summary",
    "",
    `- Page variants checked: ${report.summary.pageVariants}`,
    `- Page load errors: ${report.summary.pageErrors}`,
    `- Console errors: ${report.summary.consoleErrors}`,
    `- Failed requests: ${report.summary.failedRequests}`,
    `- Horizontal overflow pages: ${report.summary.horizontalOverflowPages}`,
    `- Unexpected Japanese text candidates: ${report.summary.japaneseTextCandidates}`,
    `- Expected bilingual/brand Japanese text candidates: ${report.summary.expectedJapaneseTextCandidates}`,
    "",
    "## Japanese Text Candidates",
    "",
  ];

  if (report.japaneseTextCandidates.length === 0) {
    lines.push("No visible Japanese text candidates were detected in the EN locale sweep.", "");
  } else {
    lines.push("| Route | Viewport | Theme | Text | Selector |");
    lines.push("| --- | --- | --- | --- | --- |");
    for (const item of report.japaneseTextCandidates) {
      lines.push(
        `| ${escapeCell(item.route)} | ${escapeCell(item.viewport)} | ${escapeCell(item.theme)} | ${escapeCell(item.text)} | ${escapeCell(item.selector)} |`
      );
    }
    lines.push("");
  }

  lines.push("## Horizontal Overflow", "");
  const overflowPages = report.pages.filter((page) => page.horizontalOverflow);
  if (overflowPages.length === 0) {
    lines.push("No page-level horizontal overflow was detected.", "");
  } else {
    lines.push("| Route | Viewport | Theme | Scroll width | Client width | Elements |");
    lines.push("| --- | --- | --- | ---: | ---: | --- |");
    for (const page of overflowPages) {
      lines.push(
        `| ${escapeCell(page.route)} | ${escapeCell(page.viewport)} | ${escapeCell(page.theme)} | ${page.scrollWidth} | ${page.clientWidth} | ${escapeCell(page.overflowElements.map((item) => `${item.selector}: ${item.width}px`).join("<br>"))} |`
      );
    }
    lines.push("");
  }

  lines.push("## Page Results", "");
  lines.push("| Route | Viewport | Theme | Status | Japanese candidates | Overflow | Console errors | Failed requests |");
  lines.push("| --- | --- | --- | --- | ---: | --- | ---: | ---: |");
  for (const page of report.pages) {
    lines.push(
      `| ${escapeCell(page.route)} | ${escapeCell(page.viewport)} | ${escapeCell(page.theme)} | ${escapeCell(page.status)} | ${page.japaneseTextCandidates.length} | ${page.horizontalOverflow ? "yes" : "no"} | ${page.consoleErrors.length} | ${page.failedRequests.length} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- GunjoUI intentionally keeps some secondary labels bilingual in navigation/catalog surfaces. Those expected samples are counted separately and omitted from the candidate table.",
    "- This audit checks rendered text and page-level overflow. It does not validate translation quality or every interactive overlay state."
  );

  return `${lines.join("\n")}\n`;
}

async function scanPage(browser, options, routeItem, viewportName, theme) {
  const viewport = VIEWPORTS[viewportName];
  const page = await browser.newPage();
  await page.setViewport(viewport);

  const consoleErrors = [];
  const failedRequests = [];
  page.on("console", (message) => {
    if (message.type() === "error") {
      consoleErrors.push(shortText(message.text(), 220));
    }
  });
  page.on("requestfailed", (request) => {
    failedRequests.push({
      url: request.url(),
      failure: request.failure()?.errorText ?? "request failed",
    });
  });
  page.on("response", (response) => {
    const status = response.status();
    if (status >= 400) {
      failedRequests.push({
        url: response.url(),
        failure: `HTTP ${status}`,
      });
    }
  });

  await page.evaluateOnNewDocument(
    (nextTheme) => {
      try {
        localStorage.setItem("gunjo-docs-locale", "en");
        localStorage.setItem("theme", nextTheme);
      } catch {
        // localStorage can be unavailable in unusual browser contexts.
      }
      document.documentElement.lang = "en";
      document.documentElement.classList.toggle("dark", nextTheme === "dark");
      document.documentElement.style.colorScheme = nextTheme;
    },
    theme
  );

  const targetUrl = new URL(routeItem.route, options.baseUrl);
  targetUrl.searchParams.set("locale", "en");

  let status = "ok";
  try {
    const response = await page.goto(targetUrl.toString(), {
      waitUntil: "networkidle2",
      timeout: options.timeoutMs,
    });
    if (!response || response.status() >= 500) {
      status = response ? `HTTP ${response.status()}` : "no response";
    }
  } catch (error) {
    status = shortText(error instanceof Error ? error.message : String(error));
  }

  await page.evaluate(
    (nextTheme) => {
      document.documentElement.lang = "en";
      document.documentElement.classList.toggle("dark", nextTheme === "dark");
      document.documentElement.style.colorScheme = nextTheme;
      try {
        localStorage.setItem("gunjo-docs-locale", "en");
        localStorage.setItem("theme", nextTheme);
      } catch {
        // ignore
      }
    },
    theme
  ).catch(() => {});
  await delay(options.settleMs);

  const rendered = await page.evaluate((japanesePatternSource) => {
    const japanesePattern = new RegExp(japanesePatternSource);

    function elementSelector(element) {
      if (!(element instanceof Element)) return "unknown";
      if (element.id) return `#${CSS.escape(element.id)}`;
      const parts = [];
      let node = element;
      while (node && node.nodeType === Node.ELEMENT_NODE && parts.length < 4) {
        const tag = node.tagName.toLowerCase();
        const role = node.getAttribute("role");
        const data = [...node.attributes]
          .map((attr) => attr.name)
          .find((name) => name.startsWith("data-"));
        let part = tag;
        if (role) part += `[role="${role}"]`;
        if (data) part += `[${data}]`;
        parts.unshift(part);
        node = node.parentElement;
      }
      return parts.join(" > ");
    }

    function isVisible(element) {
      if (!(element instanceof Element)) return false;
      const style = window.getComputedStyle(element);
      if (style.display === "none" || style.visibility === "hidden" || Number(style.opacity) === 0) {
        return false;
      }
      const rect = element.getBoundingClientRect();
      return rect.width > 0 && rect.height > 0;
    }

    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const text = node.nodeValue?.replace(/\s+/g, " ").trim() ?? "";
        if (!text || !japanesePattern.test(text)) return NodeFilter.FILTER_REJECT;
        const parent = node.parentElement;
        if (!parent || !isVisible(parent)) return NodeFilter.FILTER_REJECT;
        const tag = parent.tagName.toLowerCase();
        if (["script", "style", "noscript"].includes(tag)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      },
    });

    const japaneseTextCandidates = [];
    const seenText = new Set();
    while (walker.nextNode()) {
      const node = walker.currentNode;
      const text = node.nodeValue?.replace(/\s+/g, " ").trim() ?? "";
      const parent = node.parentElement;
      const key = `${text}|${elementSelector(parent)}`;
      if (seenText.has(key)) continue;
      seenText.add(key);
      japaneseTextCandidates.push({
        text: text.length > 160 ? `${text.slice(0, 159)}…` : text,
        selector: elementSelector(parent),
      });
    }

    const root = document.documentElement;
    const body = document.body;
    const clientWidth = root.clientWidth;
    const scrollWidth = Math.max(root.scrollWidth, body.scrollWidth);
    const horizontalOverflow = scrollWidth > clientWidth + 2;
    const overflowElements = [];
    if (horizontalOverflow) {
      for (const element of Array.from(document.body.querySelectorAll("*"))) {
        if (!(element instanceof HTMLElement) || !isVisible(element)) continue;
        const rect = element.getBoundingClientRect();
        if (rect.right > clientWidth + 2 || rect.left < -2) {
          overflowElements.push({
            selector: elementSelector(element),
            width: Math.round(rect.width),
            left: Math.round(rect.left),
            right: Math.round(rect.right),
          });
          if (overflowElements.length >= 8) break;
        }
      }
    }

    return {
      lang: document.documentElement.lang,
      clientWidth,
      scrollWidth,
      horizontalOverflow,
      overflowElements,
      japaneseTextCandidates,
    };
  }, JAPANESE_TEXT_PATTERN.source).catch((error) => ({
    lang: "unknown",
    clientWidth: 0,
    scrollWidth: 0,
    horizontalOverflow: false,
    overflowElements: [],
    japaneseTextCandidates: [{
      text: `evaluation failed: ${shortText(error instanceof Error ? error.message : String(error))}`,
      selector: "script",
    }],
  }));

  await page.close();

  const expectedFailedRequests = failedRequests.filter((request) =>
    isExpectedFailedRequest(routeItem, request)
  );
  const unexpectedFailedRequests = failedRequests.filter(
    (request) => !isExpectedFailedRequest(routeItem, request)
  );
  const expectedConsoleErrors = consoleErrors.filter((message) =>
    isExpectedConsoleError(routeItem, message)
  );
  const unexpectedConsoleErrors = consoleErrors.filter(
    (message) => !isExpectedConsoleError(routeItem, message)
  );
  const expectedJapaneseTextCandidates = rendered.japaneseTextCandidates.filter((item) =>
    isExpectedJapaneseCandidate(routeItem.route, item.text, item.selector)
  );
  const unexpectedJapaneseTextCandidates = rendered.japaneseTextCandidates.filter(
    (item) => !isExpectedJapaneseCandidate(routeItem.route, item.text, item.selector)
  );

  return {
    route: routeItem.route,
    label: routeItem.label,
    viewport: viewportName,
    theme,
    status,
    lang: rendered.lang,
    clientWidth: rendered.clientWidth,
    scrollWidth: rendered.scrollWidth,
    horizontalOverflow: rendered.horizontalOverflow,
    overflowElements: rendered.overflowElements,
    japaneseTextCandidates: unexpectedJapaneseTextCandidates,
    expectedJapaneseTextCandidates,
    consoleErrors: unexpectedConsoleErrors,
    expectedConsoleErrors,
    failedRequests: unexpectedFailedRequests,
    expectedFailedRequests,
  };
}

async function main() {
  const options = parseOptions();
  const browser = await puppeteer.launch({ headless: true });
  const pages = [];

  try {
    for (const routeItem of options.routes) {
      for (const viewportName of options.viewports) {
        for (const theme of options.themes) {
          pages.push(await scanPage(browser, options, routeItem, viewportName, theme));
        }
      }
    }
  } finally {
    await browser.close();
  }

  const japaneseTextCandidates = pages.flatMap((page) =>
    page.japaneseTextCandidates.map((item) => ({
      route: page.route,
      viewport: page.viewport,
      theme: page.theme,
      text: item.text,
      selector: item.selector,
    }))
  );
  const expectedJapaneseTextCandidates = pages.flatMap((page) =>
    page.expectedJapaneseTextCandidates.map((item) => ({
      route: page.route,
      viewport: page.viewport,
      theme: page.theme,
      text: item.text,
      selector: item.selector,
    }))
  );
  const report = {
    generatedAt: new Date().toISOString(),
    baseUrl: options.baseUrl,
    routes: options.routes.map((item) => item.route),
    viewports: options.viewports,
    themes: options.themes,
    summary: {
      pageVariants: pages.length,
      pageErrors: pages.filter((page) => page.status !== "ok").length,
      consoleErrors: pages.reduce((sum, page) => sum + page.consoleErrors.length, 0),
      failedRequests: pages.reduce((sum, page) => sum + page.failedRequests.length, 0),
      horizontalOverflowPages: pages.filter((page) => page.horizontalOverflow).length,
      japaneseTextCandidates: japaneseTextCandidates.length,
      expectedJapaneseTextCandidates: expectedJapaneseTextCandidates.length,
    },
    pages,
    japaneseTextCandidates,
    expectedJapaneseTextCandidates,
  };

  writeFileSync(options.jsonPath, `${JSON.stringify(report, null, 2)}\n`);
  writeFileSync(options.reportPath, buildMarkdown(report));

  if (
    report.summary.pageErrors > 0 ||
    report.summary.consoleErrors > 0 ||
    report.summary.failedRequests > 0 ||
    report.summary.horizontalOverflowPages > 0 ||
    report.summary.japaneseTextCandidates > 0
  ) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
