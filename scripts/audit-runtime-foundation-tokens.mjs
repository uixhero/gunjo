#!/usr/bin/env node

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import {
  collectFoundationUsage,
  escapeCell,
  listSourceFiles,
} from "./audit-foundation-token-utils.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_PATH = join(ROOT, "docs/runtime-foundation-token-audit.md");
const AUDIT_ISSUE = 267;

const TARGET_PATHS = [
  "src/components/inputs",
  "src/components/display",
  "src/components/feedback",
  "src/components/navigation",
  "src/components/overlay",
  "src/components/layout",
  "src/components/patterns",
  "app/components",
  "app/lib",
  "app/patterns/_lib",
  "app/patterns/auth/_lib",
  "app/patterns/dashboard/_lib",
  "app/patterns/media-library/_lib",
];

function auditFiles() {
  return [
    ...new Set(
      TARGET_PATHS.flatMap((targetPath) => listSourceFiles(join(ROOT, targetPath)))
    ),
  ]
    .sort((a, b) => a.localeCompare(b))
    .filter((filePath) => existsSync(filePath))
    .map((filePath) => ({
      file: relative(ROOT, filePath),
      usage: collectFoundationUsage(readFileSync(filePath, "utf-8")),
    }));
}

function countWith(results, key) {
  return results.filter((item) => item.usage[key].length > 0).length;
}

function buildMarkdown(results) {
  const generated = new Date().toISOString().slice(0, 10);
  const lines = [
    "# Runtime Foundation Token Audit",
    "",
    `Generated: ${generated}`,
    "",
    `Parent issue: #${AUDIT_ISSUE}`,
    "",
    "This report is generated by `npm run docs:audit:runtime-foundation-tokens`.",
    "",
    "Scope: shared runtime/demo helpers under `src/components/**`, `app/components/**`, `app/lib/**`, and pattern `_lib` folders. Component docs pages and site pages are covered by separate reports.",
    "",
    "## Summary",
    "",
    `- Runtime files: ${results.length}`,
    `- Files with arbitrary typography classes: ${countWith(results, "typographyArbitrary")}`,
    `- Files with arbitrary spacing/layout classes: ${countWith(results, "spacingArbitrary")}`,
    `- Files with arbitrary radius classes: ${countWith(results, "radiusArbitrary")}`,
    `- Files with arbitrary shadow classes: ${countWith(results, "shadowArbitrary")}`,
    `- Files with intrinsic inline geometry styles: ${countWith(results, "inlineGeometry")}`,
    `- Files with inline foundation styles needing review: ${countWith(results, "inlineReview")}`,
    "",
    "## Matrix",
    "",
    "| File | Typography | Typography arbitrary | Spacing/layout | Spacing arbitrary | Radius | Radius arbitrary | Shadow | Shadow arbitrary | Intrinsic inline geometry | Inline review styles |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
  ];

  for (const item of results) {
    lines.push(
      `| ${escapeCell(item.file)} | ${escapeCell(item.usage.typography)} | ${escapeCell(item.usage.typographyArbitrary)} | ${escapeCell(item.usage.spacing)} | ${escapeCell(item.usage.spacingArbitrary)} | ${escapeCell(item.usage.radius)} | ${escapeCell(item.usage.radiusArbitrary)} | ${escapeCell(item.usage.shadow)} | ${escapeCell(item.usage.shadowArbitrary)} | ${escapeCell(item.usage.inlineGeometry)} | ${escapeCell(item.usage.inlineReview)} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- Standard Tailwind scale utilities are treated as tokenized usage.",
    "- Arbitrary classes and non-geometry inline style literals should be reviewed before replacement.",
    "- Inline geometry styles are listed for visibility but are usually intrinsic to data visualization, measured layout, or documented frame sizing."
  );

  return `${lines.join("\n")}\n`;
}

function main() {
  const results = auditFiles();
  writeFileSync(OUTPUT_PATH, buildMarkdown(results), "utf-8");
  console.log(`runtime-foundation-token-audit: wrote ${relative(ROOT, OUTPUT_PATH)}`);
}

main();
