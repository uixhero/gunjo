#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { spawn } from "node:child_process";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const DEFAULT_BASE_URL = "https://gunjo.vercel.app";
const OUTPUT_MD = join(ROOT, "docs", "public-lighthouse-audit.md");
const OUTPUT_JSON = join(ROOT, "docs", "public-lighthouse-audit.json");
const LIGHTHOUSE_CLI = join(ROOT, "node_modules", "lighthouse", "cli", "index.js");

const ROUTES = [
  { id: "home", label: "トップ", path: "/" },
  { id: "showcase", label: "Showcase", path: "/showcase" },
  { id: "docs-introduction", label: "Docs: はじめに", path: "/docs/introduction" },
  { id: "docs-button", label: "Docs: Button", path: "/docs/components/button" },
  { id: "pattern-dashboard", label: "Pattern: Dashboard", path: "/patterns/dashboard/overview" },
  { id: "pattern-auth", label: "Pattern: Auth", path: "/patterns/auth/login" },
  { id: "pattern-media-library", label: "Pattern: Media Library", path: "/patterns/media-library" },
];

const PROFILES = [
  {
    id: "mobile",
    label: "Mobile",
    args: [],
  },
  {
    id: "desktop",
    label: "Desktop",
    args: ["--preset=desktop"],
  },
];

const PRIORITY_AUDITS = [
  "largest-contentful-paint",
  "cumulative-layout-shift",
  "total-blocking-time",
  "unused-javascript",
  "uses-optimized-images",
  "uses-responsive-images",
  "modern-image-formats",
  "properly-size-images",
  "offscreen-images",
  "font-display",
];

function getArg(name, fallback = null) {
  const prefix = `--${name}=`;
  const found = process.argv.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : fallback;
}

function toUrl(baseUrl, path) {
  return new URL(path, baseUrl).toString();
}

function score(value) {
  if (typeof value !== "number") return null;
  return Math.round(value * 100);
}

function metric(audit) {
  if (!audit) return null;
  return {
    id: audit.id,
    title: audit.title,
    score: score(audit.score),
    displayValue: audit.displayValue ?? "",
    numericValue: audit.numericValue ?? null,
  };
}

function escapeCell(value) {
  const text = String(value ?? "");
  return text ? text.replace(/\|/g, "\\|").replace(/\n/g, "<br>") : "-";
}

function formatScore(value) {
  if (value === null || value === undefined) return "-";
  return String(value);
}

function gradePriority(result) {
  const scores = Object.values(result.categories).filter((value) => typeof value === "number");
  const lowestScore = Math.min(...scores);
  const lcp = result.metrics["largest-contentful-paint"]?.numericValue ?? 0;
  const tbt = result.metrics["total-blocking-time"]?.numericValue ?? 0;
  const cls = result.metrics["cumulative-layout-shift"]?.numericValue ?? 0;

  if (lowestScore < 70 || lcp > 4000 || tbt > 600 || cls > 0.25) return "P1";
  if (lowestScore < 90 || lcp > 2500 || tbt > 200 || cls > 0.1) return "P2";
  return "P3";
}

function summarizeOpportunities(result) {
  const opportunities = [];

  for (const id of PRIORITY_AUDITS) {
    const audit = result.metrics[id];
    if (!audit) continue;
    if (audit.score !== null && audit.score < 90) {
      opportunities.push(`${audit.title}${audit.displayValue ? ` (${audit.displayValue})` : ""}`);
    }
  }

  const diagnostics = result.rawAudits?.diagnostics?.details?.items?.[0];
  if (diagnostics?.numScripts && diagnostics.numScripts > 80) {
    opportunities.push(`script count ${diagnostics.numScripts}`);
  }
  if (diagnostics?.totalByteWeight && diagnostics.totalByteWeight > 2_000_000) {
    opportunities.push(`total byte weight ${(diagnostics.totalByteWeight / 1024 / 1024).toFixed(1)} MiB`);
  }

  return opportunities.slice(0, 6);
}

function buildMarkdown(report) {
  const lines = [
    "# Public Lighthouse Audit",
    "",
    `Generated: ${report.generatedAt}`,
    "",
    `Base URL: ${report.baseUrl}`,
    "",
    "Parent issue: #280",
    "",
    "This report is generated by `npm run docs:audit:lighthouse-public`.",
    "",
    "## Summary",
    "",
    "| Page | Profile | Performance | Accessibility | Best Practices | SEO | LCP | TBT | CLS | Priority |",
    "| --- | --- | ---: | ---: | ---: | ---: | --- | --- | --- | --- |",
  ];

  for (const result of report.results) {
    lines.push(
      `| ${escapeCell(result.label)} | ${escapeCell(result.profileLabel)} | ${formatScore(result.categories.performance)} | ${formatScore(result.categories.accessibility)} | ${formatScore(result.categories["best-practices"])} | ${formatScore(result.categories.seo)} | ${escapeCell(result.metrics["largest-contentful-paint"]?.displayValue)} | ${escapeCell(result.metrics["total-blocking-time"]?.displayValue)} | ${escapeCell(result.metrics["cumulative-layout-shift"]?.displayValue)} | ${result.priority} |`
    );
  }

  lines.push("", "## Improvement Candidates", "");

  for (const result of report.results) {
    const opportunities = result.opportunities;
    lines.push(`### ${result.label} / ${result.profileLabel}`);
    if (opportunities.length === 0) {
      lines.push("", "- No high-priority Lighthouse opportunity in the tracked audit set.", "");
      continue;
    }
    for (const opportunity of opportunities) {
      lines.push(`- ${opportunity}`);
    }
    lines.push("");
  }

  lines.push(
    "## Notes",
    "",
    "- The audit covers the public production site, not localhost.",
    "- Scores can vary between runs because Lighthouse uses network and CPU throttling.",
    "- Priority is derived from lowest category score plus LCP/TBT/CLS thresholds."
  );

  return `${lines.join("\n")}\n`;
}

function runLighthouse({ url, profile, outputPath, timeoutMs }) {
  return new Promise((resolve, reject) => {
    const args = [
      LIGHTHOUSE_CLI,
      url,
      "--quiet",
      "--output=json",
      `--output-path=${outputPath}`,
      "--only-categories=performance,accessibility,best-practices,seo",
      "--chrome-flags=--headless=new --no-sandbox",
      ...profile.args,
    ];

    const child = spawn(process.execPath, args, {
      cwd: ROOT,
      stdio: ["ignore", "pipe", "pipe"],
    });

    let stdout = "";
    let stderr = "";
    const timer = setTimeout(() => {
      child.kill("SIGTERM");
      reject(new Error(`Lighthouse timed out for ${url} (${profile.id}) after ${timeoutMs}ms`));
    }, timeoutMs);

    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });
    child.stderr.on("data", (chunk) => {
      stderr += chunk.toString();
    });
    child.on("error", (error) => {
      clearTimeout(timer);
      reject(error);
    });
    child.on("close", (code) => {
      clearTimeout(timer);
      if (code !== 0) {
        reject(new Error(`Lighthouse failed for ${url} (${profile.id}) with code ${code}\n${stderr || stdout}`));
        return;
      }
      resolve();
    });
  });
}

async function main() {
  if (!existsSync(LIGHTHOUSE_CLI)) {
    throw new Error("lighthouse is not installed. Run npm install first.");
  }

  const options = {
    baseUrl: getArg("base-url", DEFAULT_BASE_URL),
    reportPath: getArg("report", OUTPUT_MD),
    jsonPath: getArg("json", OUTPUT_JSON),
    timeoutMs: Number(getArg("timeout", "120000")),
  };
  const tmpRoot = join(tmpdir(), `gunjo-lighthouse-${Date.now()}`);
  mkdirSync(tmpRoot, { recursive: true });

  const results = [];
  try {
    for (const route of ROUTES) {
      for (const profile of PROFILES) {
        const url = toUrl(options.baseUrl, route.path);
        const outputPath = join(tmpRoot, `${route.id}-${profile.id}.json`);
        console.log(`lighthouse ${profile.id} ${url}`);
        await runLighthouse({ url, profile, outputPath, timeoutMs: options.timeoutMs });
        const lhr = JSON.parse(readFileSync(outputPath, "utf8"));
        const result = {
          id: route.id,
          label: route.label,
          path: route.path,
          url,
          profile: profile.id,
          profileLabel: profile.label,
          finalUrl: lhr.finalUrl,
          fetchTime: lhr.fetchTime,
          categories: {
            performance: score(lhr.categories.performance?.score),
            accessibility: score(lhr.categories.accessibility?.score),
            "best-practices": score(lhr.categories["best-practices"]?.score),
            seo: score(lhr.categories.seo?.score),
          },
          metrics: Object.fromEntries(PRIORITY_AUDITS.map((id) => [id, metric(lhr.audits[id])])),
          rawAudits: {
            diagnostics: lhr.audits.diagnostics,
          },
        };
        result.priority = gradePriority(result);
        result.opportunities = summarizeOpportunities(result);
        results.push(result);
      }
    }
  } finally {
    rmSync(tmpRoot, { recursive: true, force: true });
  }

  const report = {
    generatedAt: new Date().toISOString(),
    baseUrl: options.baseUrl,
    routes: ROUTES,
    profiles: PROFILES.map(({ id, label }) => ({ id, label })),
    results,
  };

  writeFileSync(options.jsonPath, `${JSON.stringify(report, null, 2)}\n`);
  writeFileSync(options.reportPath, buildMarkdown(report));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
