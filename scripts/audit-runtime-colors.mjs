#!/usr/bin/env node

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_PATH = join(ROOT, "docs/runtime-color-audit.md");
const AUDIT_ISSUE = 260;

const TARGET_PATHS = [
  "src/components/inputs",
  "src/components/display",
  "src/components/feedback",
  "src/components/navigation",
  "src/components/overlay",
  "src/components/layout",
  "src/components/patterns",
  "app/components",
  "app/lib",
  "app/patterns/_lib",
  "app/patterns/auth/_lib",
  "app/patterns/dashboard/_lib",
  "app/patterns/media-library/_lib",
];

const TOKEN_COLOR_CLASS_PATTERN =
  /\b(?:bg|text|border|ring|shadow|from|to|via|stroke|fill|outline|decoration)-(?:primary|info|success|warning|destructive|background|foreground|card|card-foreground|popover|popover-foreground|muted|muted-foreground|secondary|secondary-foreground|accent|accent-foreground|border|input|overlay|palette-[\w-]+|gunjo-[\w-]+|kobicha-[\w-]+)(?:-[\w]+)*(?:\/[0-9]{1,3})?\b/g;
const OLD_SEMANTIC_ALPHA_PATTERN =
  /\b(?:bg|text|border|ring|shadow|from|to|via|stroke|fill|outline|decoration)-(?:primary|info|success|warning|destructive)(?:-[\w]+)?\/[0-9]{1,3}\b/g;
const COLOR_SCALE_CLASS_PATTERN =
  /\b(?:bg|text|border|ring|from|to|via|shadow|drop-shadow|stroke|fill|outline|decoration)-(?:slate|gray|zinc|neutral|stone|red|orange|amber|yellow|lime|green|emerald|teal|cyan|sky|blue|indigo|violet|purple|fuchsia|pink|rose|black|white)(?:-[0-9]{2,3})?(?:\/[0-9]{1,3})?\b/g;
const ARBITRARY_COLOR_CLASS_PATTERN =
  /\b(?:bg|text|border|ring|from|to|via|shadow|drop-shadow|stroke|fill|outline|decoration)-\[[^\]]*(?:#|rgb|hsl|oklch|oklab|lab|lch)[^\]]*\]\b/g;
const CSS_VAR_PATTERN = /--[a-z0-9][a-z0-9-]*/g;
const INLINE_STYLE_COLOR_LITERAL_PATTERN =
  /\b(?:background(?:Color|Image)?|color|fill|stroke|boxShadow|filter)\s*:\s*["'`][^"'`]*(?:#(?:[0-9a-fA-F]{3,8})\b|rgba?\(|hsla?\()[^"'`]*["'`]/g;
const CONTENT_COLOR_REFERENCE_PATTERN = /#[0-9a-fA-F]{3,8}\b/g;

function unique(values) {
  return [...new Set(values.filter(Boolean))].sort((a, b) => a.localeCompare(b));
}

function escapeCell(value) {
  const text = Array.isArray(value) ? value.join("<br>") : `${value}`;
  if (!text) return "-";
  return text.replace(/\|/g, "\\|").replace(/\n/g, "<br>");
}

function listSourceFiles(rootDir) {
  if (!existsSync(rootDir)) return [];

  const files = [];
  const stack = [rootDir];
  while (stack.length > 0) {
    const current = stack.pop();
    const entries = readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      if (entry.name === "generated") continue;
      const absolutePath = join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(absolutePath);
        continue;
      }
      if (entry.isFile() && /\.(ts|tsx)$/.test(entry.name)) files.push(absolutePath);
    }
  }
  return files.sort((a, b) => a.localeCompare(b));
}

function collectUsage(source) {
  const inlineColors = unique(
    (source.match(INLINE_STYLE_COLOR_LITERAL_PATTERN) ?? []).filter(
      (fragment) => !fragment.includes("var(--") && !fragment.includes("var(${")
    )
  );
  const contentColorReferences = unique(source.match(CONTENT_COLOR_REFERENCE_PATTERN) ?? []);

  return {
    tokenClasses: unique(source.match(TOKEN_COLOR_CLASS_PATTERN) ?? []),
    oldSemanticAlpha: unique(source.match(OLD_SEMANTIC_ALPHA_PATTERN) ?? []),
    hardcodedColorClasses: unique([
      ...(source.match(COLOR_SCALE_CLASS_PATTERN) ?? []),
      ...(source.match(ARBITRARY_COLOR_CLASS_PATTERN) ?? []),
    ]),
    cssVars: unique(source.match(CSS_VAR_PATTERN) ?? []),
    inlineColors,
    contentColorReferences,
  };
}

function auditFiles() {
  return unique(
    TARGET_PATHS.flatMap((targetPath) => listSourceFiles(join(ROOT, targetPath)))
  ).map((filePath) => {
    const file = relative(ROOT, filePath);
    const source = readFileSync(filePath, "utf-8");
    return {
      file,
      usage: collectUsage(source),
    };
  });
}

function buildMarkdown(results) {
  const generated = new Date().toISOString().slice(0, 10);
  const withOldAlpha = results.filter((item) => item.usage.oldSemanticAlpha.length > 0).length;
  const withHardcoded = results.filter(
    (item) =>
      item.usage.hardcodedColorClasses.length > 0 ||
      item.usage.inlineColors.length > 0
  ).length;
  const withContentReferences = results.filter(
    (item) => item.usage.contentColorReferences.length > 0
  ).length;

  const lines = [
    "# Runtime Color Audit",
    "",
    `Generated: ${generated}`,
    "",
    `Parent issue: #${AUDIT_ISSUE}`,
    "",
    "This report is generated by `npm run docs:audit:runtime-colors`.",
    "",
    "Scope: shared runtime/demo helpers under `src/components/**`, `app/components/**`, `app/lib/**`, and pattern `_lib` folders. Component docs pages and site pages are covered by separate reports.",
    "",
    "## Summary",
    "",
    `- Runtime files: ${results.length}`,
    `- Files with old semantic alpha classes: ${withOldAlpha}`,
    `- Files with hardcoded color classes or inline color literals: ${withHardcoded}`,
    `- Files with content color references: ${withContentReferences}`,
    "",
    "## Matrix",
    "",
    "| File | Token color classes | CSS vars | Old semantic alpha | Hardcoded color classes | Inline color literals | Content color references |",
    "| --- | --- | --- | --- | --- | --- | --- |",
  ];

  for (const item of results) {
    lines.push(
      `| ${escapeCell(item.file)} | ${escapeCell(item.usage.tokenClasses)} | ${escapeCell(item.usage.cssVars)} | ${escapeCell(item.usage.oldSemanticAlpha)} | ${escapeCell(item.usage.hardcodedColorClasses)} | ${escapeCell(item.usage.inlineColors)} | ${escapeCell(item.usage.contentColorReferences)} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- `Content color references` are literal color values in comments, demo copy, or generated descriptions, not style usage.",
    "- SSOT-generated `component-style-hints` and `design/component-specs/*.json` intentionally retain concrete color hints and are not part of this runtime audit.",
    "- `Old semantic alpha` and `Hardcoded color classes` should be zero for runtime UI code."
  );

  return `${lines.join("\n")}\n`;
}

function main() {
  const results = auditFiles();
  writeFileSync(OUTPUT_PATH, buildMarkdown(results));
  console.log(`runtime-color-audit: wrote ${relative(ROOT, OUTPUT_PATH)}`);
}

main();
