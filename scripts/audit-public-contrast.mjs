#!/usr/bin/env node

import { writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import puppeteer from "puppeteer";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_MD = join(ROOT, "docs", "public-contrast-audit.md");
const OUTPUT_JSON = join(ROOT, "docs", "public-contrast-audit.json");
const DEFAULT_BASE_URL = "http://127.0.0.1:13030";
const MIN_NORMAL = 4.5;
const MIN_LARGE = 3;

const DEFAULT_ROUTES = [
  { route: "/", label: "Home" },
  { route: "/showcase", label: "Showcase" },
  { route: "/docs/introduction", label: "Docs introduction" },
  { route: "/docs/components/button", label: "Docs component representative" },
  { route: "/docs/colors", label: "Colors" },
  { route: "/docs/tokens", label: "Tokens" },
  { route: "/patterns", label: "Patterns index" },
  { route: "/patterns/dashboard/overview", label: "Dashboard pattern" },
  { route: "/patterns/auth/login", label: "Auth pattern" },
  { route: "/patterns/media-library", label: "Media library pattern" },
  { route: "/patterns/not-found", label: "Not found pattern" },
  { route: "/missing-public-readiness-page", label: "App 404" },
];

function delay(ms) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms);
  });
}

function getArg(name, fallback = null) {
  const prefix = `--${name}=`;
  const found = process.argv.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : fallback;
}

function hasArg(name) {
  return process.argv.includes(`--${name}`);
}

function parseRoutes() {
  const routesArg = getArg("routes");
  if (!routesArg) return DEFAULT_ROUTES;
  return routesArg.split(",").map((route) => ({
    route: route.trim(),
    label: route.trim(),
  })).filter((item) => item.route);
}

function escapeCell(value) {
  const text = `${value ?? ""}`;
  return text ? text.replace(/\|/g, "\\|").replace(/\n/g, "<br>") : "-";
}

function formatRatio(value) {
  return Number.isFinite(value) ? `${value.toFixed(2)}:1` : "-";
}

function buildMarkdown(report) {
  const lines = [
    "# Public Contrast Audit",
    "",
    `Generated: ${report.generatedAt}`,
    "",
    "Parent issue: #279",
    "",
    "This report is generated by `npm run docs:audit:public-contrast`.",
    "",
    "## Scope",
    "",
    `- Base URL: ${report.baseUrl}`,
    `- Viewport: ${report.viewport.width}x${report.viewport.height}`,
    `- Themes: ${report.themes.join(", ")}`,
    `- Routes: ${report.routes.length}`,
    "",
    "## Summary",
    "",
    `- Text samples checked: ${report.summary.samples}`,
    `- AA failures: ${report.summary.failures}`,
    `- Manual background reviews: ${report.summary.manualReviews}`,
    `- Page load errors: ${report.summary.pageErrors}`,
    "",
    "## AA Failures",
    "",
  ];

  if (report.failures.length === 0) {
    lines.push("No AA contrast failures detected by the automated text scan.", "");
  } else {
    lines.push("| Route | Theme | Text | Ratio | Required | Color | Background | Selector |");
    lines.push("| --- | --- | --- | --- | --- | --- | --- | --- |");
    for (const item of report.failures) {
      lines.push(
        `| ${escapeCell(item.route)} | ${escapeCell(item.theme)} | ${escapeCell(item.text)} | ${formatRatio(item.ratio)} | ${formatRatio(item.required)} | ${escapeCell(item.color)} | ${escapeCell(item.background)} | ${escapeCell(item.selector)} |`
      );
    }
    lines.push("");
  }

  lines.push("## Manual Background Reviews", "");
  if (report.manualReviews.length === 0) {
    lines.push("No gradient/image-backed text samples required manual background review.", "");
  } else {
    lines.push("| Route | Theme | Text | Color | Background reason | Selector |");
    lines.push("| --- | --- | --- | --- | --- | --- |");
    for (const item of report.manualReviews) {
      lines.push(
        `| ${escapeCell(item.route)} | ${escapeCell(item.theme)} | ${escapeCell(item.text)} | ${escapeCell(item.color)} | ${escapeCell(item.reason)} | ${escapeCell(item.selector)} |`
      );
    }
    lines.push("");
  }

  lines.push("## Page Results", "");
  lines.push("| Route | Theme | Status | Samples | Failures | Manual reviews |");
  lines.push("| --- | --- | --- | ---: | ---: | ---: |");
  for (const page of report.pages) {
    lines.push(
      `| ${escapeCell(page.route)} | ${escapeCell(page.theme)} | ${escapeCell(page.status)} | ${page.samples} | ${page.failures.length} | ${page.manualReviews.length} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- This scanner resolves `rgb()` / `rgba()` / `oklab()` text and composites semi-transparent foreground/background colors before checking contrast. It intentionally sends gradient and image-backed text samples to manual review.",
    "- Large text uses the WCAG AA 3:1 threshold; normal text uses 4.5:1.",
    "- Dynamic hover/focus/disabled states need targeted interaction checks and are not fully covered by this static page scan."
  );

  return `${lines.join("\n")}\n`;
}

async function scanPage(page, { baseUrl, route, label, theme }) {
  const url = new URL(route, baseUrl).toString();
  await page.evaluateOnNewDocument((nextTheme) => {
    try {
      localStorage.setItem("theme", nextTheme);
    } catch {
      // localStorage can be unavailable in unusual browser contexts.
    }
    document.documentElement.classList.toggle("dark", nextTheme === "dark");
    document.documentElement.style.colorScheme = nextTheme;
  }, theme);
  const response = await page.goto(url, { waitUntil: "networkidle2", timeout: 45000 });
  await page.evaluate((nextTheme) => {
    document.documentElement.classList.toggle("dark", nextTheme === "dark");
    document.documentElement.style.colorScheme = nextTheme;
    try {
      localStorage.setItem("theme", nextTheme);
    } catch {
      // localStorage can be unavailable in unusual browser contexts.
    }
  }, theme);
  await page.waitForFunction(
    (nextTheme) => document.documentElement.classList.contains("dark") === (nextTheme === "dark"),
    { timeout: 5000 },
    theme
  ).catch(() => {});
  await delay(150);

  const data = await page.evaluate(({ minNormal, minLarge }) => {
    function clamp01(value) {
      return Math.min(1, Math.max(0, value));
    }

    function linearToSrgb(channel) {
      const value = clamp01(channel);
      return value <= 0.0031308
        ? 12.92 * value
        : 1.055 * Math.pow(value, 1 / 2.4) - 0.055;
    }

    function parseRgb(value) {
      const match = /^rgba?\(([^)]+)\)$/.exec(value);
      if (!match) return null;
      const parts = match[1].split(",").map((part) => part.trim());
      if (parts.length < 3) return null;
      const [r, g, b] = parts.slice(0, 3).map(Number);
      const alpha = parts[3] == null ? 1 : Number(parts[3]);
      if ([r, g, b, alpha].some((part) => Number.isNaN(part))) return null;
      return { r, g, b, a: alpha };
    }

    function parseOklab(value) {
      const match = /^oklab\(([^)]+)\)$/.exec(value);
      if (!match) return null;
      const [channels, alphaPart] = match[1].split("/").map((part) => part.trim());
      const parts = channels.split(/\s+/).filter(Boolean);
      if (parts.length < 3) return null;
      const [L, A, B] = parts.slice(0, 3).map(Number);
      const alpha = alphaPart == null ? 1 : Number(alphaPart);
      if ([L, A, B, alpha].some((part) => Number.isNaN(part))) return null;

      const lPrime = L + 0.3963377774 * A + 0.2158037573 * B;
      const mPrime = L - 0.1055613458 * A - 0.0638541728 * B;
      const sPrime = L - 0.0894841775 * A - 1.2914855480 * B;
      const l = lPrime ** 3;
      const m = mPrime ** 3;
      const s = sPrime ** 3;

      const rLinear = 4.0767416621 * l - 3.3077115913 * m + 0.2309699292 * s;
      const gLinear = -1.2684380046 * l + 2.6097574011 * m - 0.3413193965 * s;
      const bLinear = -0.0041960863 * l - 0.7034186147 * m + 1.7076147010 * s;

      return {
        r: Math.round(linearToSrgb(rLinear) * 255),
        g: Math.round(linearToSrgb(gLinear) * 255),
        b: Math.round(linearToSrgb(bLinear) * 255),
        a: alpha,
      };
    }

    function parseColor(value) {
      return parseRgb(value) ?? parseOklab(value);
    }

    function blendColor(foreground, background) {
      const alpha = clamp01(foreground.a);
      return {
        r: foreground.r * alpha + background.r * (1 - alpha),
        g: foreground.g * alpha + background.g * (1 - alpha),
        b: foreground.b * alpha + background.b * (1 - alpha),
        a: 1,
      };
    }

    function channelToLinear(channel) {
      const value = channel / 255;
      return value <= 0.03928
        ? value / 12.92
        : Math.pow((value + 0.055) / 1.055, 2.4);
    }

    function luminance(rgb) {
      return (
        0.2126 * channelToLinear(rgb.r) +
        0.7152 * channelToLinear(rgb.g) +
        0.0722 * channelToLinear(rgb.b)
      );
    }

    function contrastRatio(foreground, background) {
      const light = Math.max(luminance(foreground), luminance(background));
      const dark = Math.min(luminance(foreground), luminance(background));
      return (light + 0.05) / (dark + 0.05);
    }

    function hasComplexBackground(style) {
      const image = style.backgroundImage;
      return image && image !== "none";
    }

    function formatRgb(color) {
      return `rgb(${Math.round(color.r)}, ${Math.round(color.g)}, ${Math.round(color.b)})`;
    }

    function resolveBackground(element) {
      if (element.closest(".on-hero, .on-hero-strong")) {
        return { type: "manual", reason: "hero image or overlay background" };
      }

      const chain = [];
      let current = element;
      while (current && current.nodeType === Node.ELEMENT_NODE) {
        chain.unshift(current);
        current = current.parentElement;
      }

      let background = { r: 255, g: 255, b: 255, a: 1 };
      let raw = "rgb(255, 255, 255)";
      let complexUnderlay = null;

      for (const item of chain) {
        const style = window.getComputedStyle(item);
        if (hasComplexBackground(style)) {
          complexUnderlay = `complex background: ${style.backgroundImage.slice(0, 72)}`;
        }
        const parsed = parseColor(style.backgroundColor);
        if (parsed && parsed.a > 0) {
          background = blendColor(parsed, background);
          raw = parsed.a >= 0.98 ? style.backgroundColor : `${style.backgroundColor} over ${raw}`;
          if (parsed.a >= 0.98) {
            complexUnderlay = null;
          }
        }
      }

      if (complexUnderlay) {
        return { type: "manual", reason: complexUnderlay };
      }

      return { type: "color", value: background, raw: raw.includes(" over ") ? formatRgb(background) : raw };
    }

    function isVisible(element) {
      let current = element;
      while (current && current.nodeType === Node.ELEMENT_NODE) {
        const style = window.getComputedStyle(current);
        if (
          style.display === "none" ||
          style.visibility === "hidden" ||
          style.opacity === "0" ||
          style.color === "transparent"
        ) {
          return false;
        }
        current = current.parentElement;
      }
      const rect = element.getBoundingClientRect();
      return rect.width > 0 && rect.height > 0;
    }

    function selectorFor(element) {
      const parts = [];
      let current = element;
      while (current && current.nodeType === Node.ELEMENT_NODE && parts.length < 4) {
        const tag = current.tagName.toLowerCase();
        const id = current.id ? `#${current.id}` : "";
        const className = typeof current.className === "string"
          ? current.className
              .split(/\s+/)
              .filter(Boolean)
              .slice(0, 2)
              .map((item) => `.${CSS.escape(item)}`)
              .join("")
          : "";
        parts.unshift(`${tag}${id}${className}`);
        current = current.parentElement;
      }
      return parts.join(" > ");
    }

    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const text = node.textContent?.replace(/\s+/g, " ").trim() ?? "";
        if (!text || text.length < 2) return NodeFilter.FILTER_REJECT;
        const parent = node.parentElement;
        if (!parent || !isVisible(parent)) return NodeFilter.FILTER_REJECT;
        if (parent.closest("[aria-hidden='true'], script, style, noscript")) {
          return NodeFilter.FILTER_REJECT;
        }
        return NodeFilter.FILTER_ACCEPT;
      },
    });

    const failures = [];
    const manualReviews = [];
    let samples = 0;
    const seen = new Set();

    while (walker.nextNode()) {
      const node = walker.currentNode;
      const element = node.parentElement;
      const text = node.textContent.replace(/\s+/g, " ").trim();
      const key = `${text}::${selectorFor(element)}`;
      if (seen.has(key)) continue;
      seen.add(key);
      samples += 1;

      const style = window.getComputedStyle(element);
      const color = parseColor(style.color);
      if (!color) {
        manualReviews.push({
          text: text.slice(0, 80),
          color: style.color,
          reason: "unsupported foreground color",
          selector: selectorFor(element),
        });
        continue;
      }

      const background = resolveBackground(element);
      if (background.type === "manual") {
        manualReviews.push({
          text: text.slice(0, 80),
          color: style.color,
          reason: background.reason,
          selector: selectorFor(element),
        });
        continue;
      }

      const fontSize = Number.parseFloat(style.fontSize);
      const fontWeight = Number.parseInt(style.fontWeight, 10) || 400;
      const isLarge = fontSize >= 24 || (fontSize >= 18.66 && fontWeight >= 700);
      const required = isLarge ? minLarge : minNormal;
      const foreground = color.a < 0.98 ? blendColor(color, background.value) : color;
      const ratio = contrastRatio(foreground, background.value);
      if (ratio < required) {
        failures.push({
          text: text.slice(0, 80),
          ratio,
          required,
          color: color.a < 0.98 ? `${style.color} over ${background.raw}` : style.color,
          background: background.raw,
          fontSize,
          fontWeight,
          selector: selectorFor(element),
        });
      }
    }

    return { samples, failures, manualReviews };
  }, { minNormal: MIN_NORMAL, minLarge: MIN_LARGE });

  return {
    route,
    label,
    theme,
    url,
    status: response?.status() ?? null,
    ...data,
  };
}

async function main() {
  const baseUrl = getArg("base-url", DEFAULT_BASE_URL);
  const routes = parseRoutes();
  const themes = getArg("themes", "light,dark")
    .split(",")
    .map((theme) => theme.trim())
    .filter(Boolean);
  const viewportArg = getArg("viewport", "1440x1100");
  const [width, height] = viewportArg.split("x").map(Number);
  const failOnViolation = hasArg("fail-on-violation");

  const browser = await puppeteer.launch({ headless: "new" });
  const pages = [];
  const pageErrors = [];

  try {
    for (const route of routes) {
      for (const theme of themes) {
        const page = await browser.newPage();
        await page.setViewport({ width, height, deviceScaleFactor: 1 });
        try {
          pages.push(await scanPage(page, {
            baseUrl,
            route: route.route,
            label: route.label,
            theme,
          }));
        } catch (error) {
          pageErrors.push({
            route: route.route,
            label: route.label,
            theme,
            message: error instanceof Error ? error.message : String(error),
          });
        } finally {
          await page.close();
        }
      }
    }
  } finally {
    await browser.close();
  }

  const failures = pages.flatMap((page) =>
    page.failures.map((item) => ({
      route: page.route,
      label: page.label,
      theme: page.theme,
      ...item,
    }))
  );
  const manualReviews = pages.flatMap((page) =>
    page.manualReviews.map((item) => ({
      route: page.route,
      label: page.label,
      theme: page.theme,
      ...item,
    }))
  );
  const report = {
    generatedAt: new Date().toISOString(),
    baseUrl,
    viewport: { width, height },
    themes,
    routes,
    summary: {
      samples: pages.reduce((total, page) => total + page.samples, 0),
      failures: failures.length,
      manualReviews: manualReviews.length,
      pageErrors: pageErrors.length,
    },
    pages,
    failures,
    manualReviews,
    pageErrors,
  };

  writeFileSync(OUTPUT_JSON, `${JSON.stringify(report, null, 2)}\n`);
  writeFileSync(OUTPUT_MD, buildMarkdown(report));
  console.log(`public-contrast-audit: wrote ${relative(ROOT, OUTPUT_MD)}`);
  console.log(`public-contrast-audit: wrote ${relative(ROOT, OUTPUT_JSON)}`);
  console.log(
    `public-contrast-audit: ${failures.length} failures, ${manualReviews.length} manual reviews, ${pageErrors.length} page errors`
  );

  if (failOnViolation && (failures.length > 0 || pageErrors.length > 0)) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
