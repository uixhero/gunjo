#!/usr/bin/env node

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import {
  collectExtendedTokenUsage,
  escapeCell,
  needsExtendedTokenReview,
} from "./audit-extended-token-utils.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const APP_DIR = join(ROOT, "app");
const OUTPUT_PATH = join(ROOT, "docs/site-extended-token-audit.md");
const AUDIT_ISSUE = 269;

const EXCLUDED_PAGE_PREFIXES = [
  "app/docs/components/",
  "app/embed/",
];

function listPages(rootDir) {
  if (!existsSync(rootDir)) return [];

  const files = [];
  const stack = [rootDir];
  while (stack.length > 0) {
    const current = stack.pop();
    const entries = readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      const absolutePath = join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(absolutePath);
        continue;
      }
      if (entry.isFile() && entry.name === "page.tsx") files.push(absolutePath);
    }
  }

  return files.sort((a, b) => a.localeCompare(b));
}

function routeFromPage(relativeFilePath) {
  const route = relativeFilePath
    .replace(/^app/, "")
    .replace(/\/page\.tsx$/, "")
    .replace(/^\//, "");
  return route.length === 0 ? "/" : `/${route}`;
}

function categoryForRoute(route) {
  if (route.startsWith("/patterns")) return "Patterns";
  if (route.startsWith("/docs")) return "Docs";
  if (route.startsWith("/ai-handoff")) return "AI handoff";
  if (route === "/") return "Home";
  return "Site";
}

function auditPages() {
  return listPages(APP_DIR)
    .map((filePath) => {
      const relativeFilePath = relative(ROOT, filePath);
      return { filePath, relativeFilePath };
    })
    .filter(({ relativeFilePath }) =>
      !EXCLUDED_PAGE_PREFIXES.some((prefix) => relativeFilePath.startsWith(prefix))
    )
    .map(({ filePath, relativeFilePath }) => {
      const route = routeFromPage(relativeFilePath);
      return {
        route,
        category: categoryForRoute(route),
        file: relativeFilePath,
        usage: collectExtendedTokenUsage(readFileSync(filePath, "utf-8")),
      };
    });
}

function countWith(results, key) {
  return results.filter((item) => item.usage[key].length > 0).length;
}

function hasInlineImplementationStyles(item) {
  return (
    item.usage.inlineMotion.length > 0 ||
    item.usage.inlineLayering.length > 0 ||
    item.usage.inlineOpacity.length > 0 ||
    item.usage.inlineBorderWidth.length > 0
  );
}

function buildMarkdown(results) {
  const generated = new Date().toISOString().slice(0, 10);
  const lines = [
    "# Site Extended Token Audit",
    "",
    `Generated: ${generated}`,
    "",
    `Parent issue: #${AUDIT_ISSUE}`,
    "",
    "This report is generated by `npm run docs:audit:site-extended-tokens`.",
    "",
    "Scope: `app/**/page.tsx`, excluding `app/docs/components/**` and `app/embed/**` because component docs are covered by `docs/component-extended-token-audit.md`.",
    "",
    "## Summary",
    "",
    `- Site/page entries: ${results.length}`,
    `- Entries with arbitrary motion classes: ${countWith(results, "motionArbitrary")}`,
    `- Entries with arbitrary layering classes: ${countWith(results, "layeringArbitrary")}`,
    `- Entries with arbitrary opacity classes: ${countWith(results, "opacityArbitrary")}`,
    `- Entries with arbitrary border width classes: ${countWith(results, "borderWidthArbitrary")}`,
    `- Entries with arbitrary container sizing classes: ${countWith(results, "containerSizingArbitrary")}`,
    `- Entries with arbitrary density classes: ${countWith(results, "densityArbitrary")}`,
    `- Entries with inline implementation styles: ${results.filter(hasInlineImplementationStyles).length}`,
    `- Entries needing review: ${results.filter(needsExtendedTokenReview).length}`,
    "",
    "## Matrix",
    "",
    "| Route | Category | File | Motion | Motion arbitrary | Layering | Layering arbitrary | Overlay position | Opacity | Opacity arbitrary | Border width | Border width arbitrary | Breakpoints | Container sizing | Container sizing arbitrary | Icon size | Density | Density arbitrary | Inline implementation styles | Inline container sizing |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
  ];

  for (const item of results) {
    const inlineReview = [
      ...item.usage.inlineMotion,
      ...item.usage.inlineLayering,
      ...item.usage.inlineOpacity,
      ...item.usage.inlineBorderWidth,
    ];
    lines.push(
      `| ${escapeCell(item.route)} | ${escapeCell(item.category)} | ${escapeCell(item.file)} | ${escapeCell(item.usage.motion)} | ${escapeCell(item.usage.motionArbitrary)} | ${escapeCell(item.usage.layering)} | ${escapeCell(item.usage.layeringArbitrary)} | ${escapeCell(item.usage.overlayPosition)} | ${escapeCell(item.usage.opacity)} | ${escapeCell(item.usage.opacityArbitrary)} | ${escapeCell(item.usage.borderWidth)} | ${escapeCell(item.usage.borderWidthArbitrary)} | ${escapeCell(item.usage.breakpoints)} | ${escapeCell(item.usage.containerSizing)} | ${escapeCell(item.usage.containerSizingArbitrary)} | ${escapeCell(item.usage.iconSize)} | ${escapeCell(item.usage.density)} | ${escapeCell(item.usage.densityArbitrary)} | ${escapeCell(inlineReview)} | ${escapeCell(item.usage.inlineContainerSizing)} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- Arbitrary classes and inline motion/border width styles should be reviewed before replacement.",
    "- Inline layering and opacity values are listed for visibility because charts, SVG layers, and overlays can require implementation-level values.",
    "- Component docs pages are intentionally excluded from this site-level report."
  );

  return `${lines.join("\n")}\n`;
}

function main() {
  const results = auditPages();
  writeFileSync(OUTPUT_PATH, buildMarkdown(results), "utf-8");
  console.log(`site-extended-token-audit: wrote ${relative(ROOT, OUTPUT_PATH)}`);
}

main();
