// Generates app/lib/thumbnail-manifest.ts from the committed thumbnail PNGs.
// Components that render a /showcase-thumbs or /patterns-thumbs <img> consult
// this so they only request thumbnails that actually exist — otherwise pages
// without a thumbnail (token/guideline/intro prev-next cards, etc.) emit 404s.
//
// Run after regenerating thumbnails (wired into `showcase:thumbs` /
// `patterns:thumbs`). A slug is listed only when BOTH light and dark exist.
import { readdirSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

function slugsWithBothModes(dir) {
    const abs = join(ROOT, dir);
    if (!existsSync(abs)) return [];
    const files = new Set(readdirSync(abs).filter((f) => f.endsWith(".png")));
    const slugs = new Set();
    for (const f of files) {
        const m = f.match(/^(.+)\.dark\.png$/);
        if (m && files.has(`${m[1]}.light.png`)) slugs.add(m[1]);
    }
    return [...slugs].sort((a, b) => a.localeCompare(b));
}

const showcase = slugsWithBothModes("public/showcase-thumbs");
const patterns = slugsWithBothModes("public/patterns-thumbs");

const src = `/**
 * Generated by \`node scripts/sync-thumb-manifest.mjs\`. Do not edit manually.
 *
 * Slugs that have BOTH light and dark thumbnails. Components gate their
 * <img src> on these sets so pages without a thumbnail never request a 404.
 */
export const SHOWCASE_THUMB_SLUGS: ReadonlySet<string> = new Set(${JSON.stringify(showcase)});
export const PATTERN_THUMB_SLUGS: ReadonlySet<string> = new Set(${JSON.stringify(patterns)});
`;

const outPath = join(ROOT, "app", "lib", "thumbnail-manifest.ts");
writeFileSync(outPath, src);
console.log(
    `thumb-manifest: wrote ${showcase.length} showcase + ${patterns.length} pattern slugs to app/lib/thumbnail-manifest.ts`
);
