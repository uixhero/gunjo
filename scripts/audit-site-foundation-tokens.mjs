#!/usr/bin/env node

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import { collectFoundationUsage, escapeCell } from "./audit-foundation-token-utils.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const APP_DIR = join(ROOT, "app");
const OUTPUT_PATH = join(ROOT, "docs/site-foundation-token-audit.md");
const AUDIT_ISSUE = 267;

const EXCLUDED_PAGE_PREFIXES = [
  "app/docs/components/",
  "app/embed/",
];

function listPages(rootDir) {
  if (!existsSync(rootDir)) return [];

  const files = [];
  const stack = [rootDir];
  while (stack.length > 0) {
    const current = stack.pop();
    const entries = readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      const absolutePath = join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(absolutePath);
        continue;
      }
      if (entry.isFile() && entry.name === "page.tsx") files.push(absolutePath);
    }
  }

  return files.sort((a, b) => a.localeCompare(b));
}

function routeFromPage(relativeFilePath) {
  const route = relativeFilePath
    .replace(/^app/, "")
    .replace(/\/page\.tsx$/, "")
    .replace(/^\//, "");
  return route.length === 0 ? "/" : `/${route}`;
}

function categoryForRoute(route) {
  if (route.startsWith("/patterns")) return "Patterns";
  if (route.startsWith("/docs")) return "Docs";
  if (route.startsWith("/ai-handoff")) return "AI handoff";
  if (route === "/") return "Home";
  return "Site";
}

function auditPages() {
  return listPages(APP_DIR)
    .map((filePath) => {
      const relativeFilePath = relative(ROOT, filePath);
      return { filePath, relativeFilePath };
    })
    .filter(({ relativeFilePath }) =>
      !EXCLUDED_PAGE_PREFIXES.some((prefix) => relativeFilePath.startsWith(prefix))
    )
    .map(({ filePath, relativeFilePath }) => {
      const route = routeFromPage(relativeFilePath);
      return {
        route,
        category: categoryForRoute(route),
        file: relativeFilePath,
        usage: collectFoundationUsage(readFileSync(filePath, "utf-8")),
      };
    });
}

function countWith(results, key) {
  return results.filter((item) => item.usage[key].length > 0).length;
}

function buildMarkdown(results) {
  const generated = new Date().toISOString().slice(0, 10);
  const lines = [
    "# Site Foundation Token Audit",
    "",
    `Generated: ${generated}`,
    "",
    `Parent issue: #${AUDIT_ISSUE}`,
    "",
    "This report is generated by `npm run docs:audit:site-foundation-tokens`.",
    "",
    "Scope: `app/**/page.tsx`, excluding `app/docs/components/**` and `app/embed/**` because component docs are covered by `docs/component-foundation-token-audit.md`.",
    "",
    "## Summary",
    "",
    `- Site/page entries: ${results.length}`,
    `- Entries with arbitrary typography classes: ${countWith(results, "typographyArbitrary")}`,
    `- Entries with arbitrary spacing/layout classes: ${countWith(results, "spacingArbitrary")}`,
    `- Entries with arbitrary radius classes: ${countWith(results, "radiusArbitrary")}`,
    `- Entries with arbitrary shadow classes: ${countWith(results, "shadowArbitrary")}`,
    `- Entries with intrinsic inline geometry styles: ${countWith(results, "inlineGeometry")}`,
    `- Entries with inline foundation styles needing review: ${countWith(results, "inlineReview")}`,
    "",
    "## Matrix",
    "",
    "| Route | Category | File | Typography | Typography arbitrary | Spacing/layout | Spacing arbitrary | Radius | Radius arbitrary | Shadow | Shadow arbitrary | Intrinsic inline geometry | Inline review styles |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
  ];

  for (const item of results) {
    lines.push(
      `| ${escapeCell(item.route)} | ${escapeCell(item.category)} | ${escapeCell(item.file)} | ${escapeCell(item.usage.typography)} | ${escapeCell(item.usage.typographyArbitrary)} | ${escapeCell(item.usage.spacing)} | ${escapeCell(item.usage.spacingArbitrary)} | ${escapeCell(item.usage.radius)} | ${escapeCell(item.usage.radiusArbitrary)} | ${escapeCell(item.usage.shadow)} | ${escapeCell(item.usage.shadowArbitrary)} | ${escapeCell(item.usage.inlineGeometry)} | ${escapeCell(item.usage.inlineReview)} |`
    );
  }

  lines.push(
    "",
    "## Notes",
    "",
    "- Standard Tailwind scale utilities are treated as tokenized usage.",
    "- Arbitrary classes and non-geometry inline style literals should be reviewed before replacement.",
    "- Component docs pages are intentionally excluded from this site-level report."
  );

  return `${lines.join("\n")}\n`;
}

function main() {
  const results = auditPages();
  writeFileSync(OUTPUT_PATH, buildMarkdown(results), "utf-8");
  console.log(`site-foundation-token-audit: wrote ${relative(ROOT, OUTPUT_PATH)}`);
}

main();
