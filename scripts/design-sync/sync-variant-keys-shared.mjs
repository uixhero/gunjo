import { unique, uniqueSorted } from "./shared.mjs";
import { toPascalCase } from "./component-source-map.mjs";

function getComponentVariantKeys(spec, componentKey) {
  return uniqueSorted(
    (spec?.components?.[componentKey]?.variants ?? [])
      .map((variant) => variant?.key)
      .filter((key) => typeof key === "string" && key.length > 0)
  );
}

function getComponentVariantKeysInSpecOrder(spec, componentKey) {
  const rawKeys = (spec?.components?.[componentKey]?.variants ?? [])
    .map((variant) => variant?.key)
    .filter((key) => typeof key === "string" && key.length > 0);
  return unique(rawKeys);
}

function getDefaultVariantKey(spec, componentKey) {
  const orderedKeys = getComponentVariantKeysInSpecOrder(spec, componentKey);
  if (orderedKeys.length === 0) return null;
  if (orderedKeys.includes("default")) return "default";
  return orderedKeys[0];
}

function renderConstArray(keys) {
  return `[${keys.map((key) => `"${key}"`).join(", ")}]`;
}

export function buildVariantKeyEntries(spec) {
  const targetComponents = Object.entries(spec?.components ?? {})
    .filter(
      ([, componentSpec]) => Array.isArray(componentSpec?.variants) && componentSpec.variants.length > 1
    )
    .map(([componentKey]) => componentKey)
    .sort((a, b) => a.localeCompare(b));

  return targetComponents.map((componentKey) => ({
    key: componentKey,
    keys: getComponentVariantKeys(spec, componentKey),
    defaultKey: getDefaultVariantKey(spec, componentKey),
  }));
}

export function renderGeneratedVariantKeyFile({ entries, groupedExportName }) {
  const lines = [
    "/* eslint-disable */",
    "// Generated by `npm run design:sync:components`. Do not edit manually.",
    "",
  ];

  for (const entry of entries) {
    const pascalName = toPascalCase(entry.key);
    lines.push(
      `export const ${entry.key}VariantKeys = ${renderConstArray(entry.keys)} as const;`
    );
    lines.push(
      `export type ${pascalName}VariantKey = (typeof ${entry.key}VariantKeys)[number];`
    );
    lines.push("");
  }

  lines.push(`export const ${groupedExportName} = {`);
  for (const entry of entries) {
    lines.push(`  ${entry.key}: ${entry.key}VariantKeys,`);
  }
  lines.push("} as const;");
  lines.push("");

  return lines.join("\n");
}

export function renderGeneratedDefaultVariantKeyFile({ entries, groupedExportName }) {
  const lines = [
    "/* eslint-disable */",
    "// Generated by `npm run design:sync:components`. Do not edit manually.",
    "",
  ];

  const typeImports = entries.map((entry) => `${toPascalCase(entry.key)}VariantKey`);
  lines.push(`import type { ${typeImports.join(", ")} } from "./variant-keys";`);
  lines.push("");

  for (const entry of entries) {
    const pascalName = toPascalCase(entry.key);
    lines.push(
      `export const ${entry.key}DefaultVariantKey: ${pascalName}VariantKey = "${entry.defaultKey}";`
    );
  }

  lines.push("");
  lines.push(`export const ${groupedExportName} = {`);
  for (const entry of entries) {
    lines.push(`  ${entry.key}: ${entry.key}DefaultVariantKey,`);
  }
  lines.push("} as const;");
  lines.push("");

  return lines.join("\n");
}
