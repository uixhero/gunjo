import { join } from "node:path";
import { ROOT, readJson, writeText } from "./shared.mjs";
import { COMPONENT_SOURCE_CATEGORY_CONFIGS } from "./component-source-map.mjs";
import {
  buildColorHint,
  buildVariantClasses,
  extractSlotIds,
} from "./component-style-hints-shared.mjs";

function getVariantKey(variant) {
  const key = variant?.key;
  if (typeof key !== "string" || key.length === 0) return null;
  return key;
}

function getVariantByKey(componentSpec) {
  const variants = Array.isArray(componentSpec?.variants) ? componentSpec.variants : [];
  const variantByKey = new Map();
  for (const variant of variants) {
    const key = getVariantKey(variant);
    if (!key || variantByKey.has(key)) continue;
    variantByKey.set(key, variant);
  }
  return variantByKey;
}

function getDefaultVariantKey(variantKeys) {
  if (variantKeys.length === 0) return null;
  return variantKeys.includes("default") ? "default" : variantKeys[0];
}

function mergeClasses(...values) {
  return [
    ...new Set(
      values
        .flatMap((value) => (typeof value === "string" ? value.split(/\s+/) : []))
        .map((token) => token.trim())
        .filter(Boolean)
    ),
  ].join(" ");
}

function buildInteractionClasses(componentSpec) {
  const classes = [];
  if (componentSpec?.interaction?.cursor === "pointer") {
    classes.push("cursor-pointer");
  }
  return classes.join(" ");
}

function buildComponentVariantClasses(componentSpec, variantSpec) {
  return mergeClasses(buildVariantClasses(variantSpec), buildInteractionClasses(componentSpec));
}

function buildVariantClassesRecord(variantKeys, variantByKey, componentSpec) {
  return Object.fromEntries(
    variantKeys.map((variantKey) => [
      variantKey,
      buildComponentVariantClasses(componentSpec, variantByKey.get(variantKey)),
    ])
  );
}

function buildVariantColorHintsRecord(variantKeys, variantByKey) {
  const entries = variantKeys
    .map((variantKey) => [variantKey, buildColorHint(variantByKey.get(variantKey))])
    .filter(([, colorHint]) => colorHint.length > 0);
  return Object.fromEntries(entries);
}

function buildComponentStyleHint(componentSpec) {
  const variantByKey = getVariantByKey(componentSpec);
  const variantKeys = [...variantByKey.keys()];
  const defaultVariantKey = getDefaultVariantKey(variantKeys);
  const baseVariant =
    (defaultVariantKey ? variantByKey.get(defaultVariantKey) : null) ??
    (variantKeys.length > 0 ? variantByKey.get(variantKeys[0]) : null);
  const baseColorHint = buildColorHint(baseVariant);

  return {
    variantKeys,
    defaultVariantKey,
    baseClasses: buildComponentVariantClasses(componentSpec, baseVariant),
    baseColorHint: baseColorHint.length > 0 ? baseColorHint : null,
    slotIds: extractSlotIds(baseVariant),
    variantClasses: buildVariantClassesRecord(variantKeys, variantByKey, componentSpec),
    variantColorHints: buildVariantColorHintsRecord(variantKeys, variantByKey),
  };
}

function buildCategoryStyleHints({ root, specPath }) {
  const spec = readJson(join(root, specPath));
  const components = spec?.components ?? {};

  return Object.fromEntries(
    Object.entries(components)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([componentKey, componentSpec]) => [componentKey, buildComponentStyleHint(componentSpec)])
  );
}

function renderComponentStyleHints(styleHints) {
  return `/* eslint-disable */\n// Generated by \`npm run design:sync:components\`. Do not edit manually.\n\nexport const componentStyleHints = ${JSON.stringify(
    styleHints,
    null,
    2
  )} as const;\n`;
}

export function syncComponentStyleHints({ root = ROOT } = {}) {
  const styleHints = Object.fromEntries(
    COMPONENT_SOURCE_CATEGORY_CONFIGS.map((config) => [
      config.category,
      buildCategoryStyleHints({ root, ...config }),
    ])
  );

  const outputPath = join(root, "src", "components", "generated", "component-style-hints.ts");
  writeText(outputPath, renderComponentStyleHints(styleHints));
  console.log(
    "design:sync: Updated src/components/generated/component-style-hints.ts from component-specs"
  );
}
