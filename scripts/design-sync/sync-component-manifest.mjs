import { join } from "node:path";
import { ROOT, readJson, writeText } from "./shared.mjs";
import {
  COMPONENT_SOURCE_CATEGORY_CONFIGS,
  getComponentFileName,
} from "./component-source-map.mjs";

const STABILITY_LEVELS = ["stable", "beta", "experimental"];
const STABILITY_DEFAULT = "experimental";

function loadStabilityMap(root) {
  try {
    const config = readJson(join(root, "design/stability.json"));
    const raw = config?.stability;
    if (!raw || typeof raw !== "object") return {};
    return Object.fromEntries(
      Object.entries(raw).filter(([, level]) => STABILITY_LEVELS.includes(level))
    );
  } catch {
    return {};
  }
}

function getVariantKeys(componentSpec) {
  if (!Array.isArray(componentSpec?.variants)) return [];
  const seen = new Set();
  const keys = [];
  for (const variant of componentSpec.variants) {
    const key = variant?.key;
    if (typeof key !== "string" || key.length === 0 || seen.has(key)) continue;
    seen.add(key);
    keys.push(key);
  }
  return keys;
}

function getDefaultVariantKey(variantKeys) {
  if (variantKeys.length === 0) return null;
  return variantKeys.includes("default") ? "default" : variantKeys[0];
}

function buildCategoryManifest({ root, category, specPath, sourceDir, fileNameOverrides, stabilityMap }) {
  const spec = readJson(join(root, specPath));
  const components = spec?.components ?? {};

  return Object.fromEntries(
    Object.entries(components)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([componentKey, componentSpec]) => {
        const variantKeys = getVariantKeys(componentSpec);
        return [
          componentKey,
          {
            title: componentSpec?.title ?? null,
            description: componentSpec?.description ?? null,
            variantKeys,
            defaultVariantKey: getDefaultVariantKey(variantKeys),
            sourceFile: `${sourceDir}/${getComponentFileName(componentKey, fileNameOverrides)}`,
            specSource: spec?.source ?? null,
            stability: stabilityMap[componentKey] ?? STABILITY_DEFAULT,
          },
        ];
      })
  );
}

function renderManifest(manifest) {
  return `/* eslint-disable */\n// Generated by \`npm run design:sync:components\`. Do not edit manually.\n\nexport const componentManifest = ${JSON.stringify(
    manifest,
    null,
    2
  )} as const;\n`;
}

export function syncComponentManifest({ root = ROOT } = {}) {
  const stabilityMap = loadStabilityMap(root);
  const manifest = Object.fromEntries(
    COMPONENT_SOURCE_CATEGORY_CONFIGS.map((config) => [
      config.category,
      buildCategoryManifest({ root, ...config, stabilityMap }),
    ])
  );

  const outputPath = join(root, "src", "components", "generated", "component-manifest.ts");
  writeText(outputPath, renderManifest(manifest));
  console.log(
    "design:sync: Updated src/components/generated/component-manifest.ts from component-specs"
  );
}
