import { join } from "node:path";
import { ROOT, readText, writeText } from "./shared.mjs";
import { extractBlock } from "./sync-standalone-tokens.mjs";

// Generates app/lib/data/token-values.generated.ts — static token values for
// docs pages (/docs/tokens, /docs/colors) so real values are present in the
// server-rendered HTML. The live pages resolve values via getComputedStyle,
// which never runs for text-only consumers (curl, AI extraction); this map is
// the static fallback. Source of truth is src/globals.css (itself generated
// by sync-tokens.mjs), same as public/tokens.css. (#687)

const DECLARATION_PATTERN = /^\s*(--[a-z0-9-]+)\s*:\s*([^;]+);\s*$/;

function parseDeclarations(blockBody) {
  const values = {};
  for (const line of blockBody.split("\n")) {
    const match = line.match(DECLARATION_PATTERN);
    if (!match) continue;
    const [, name, value] = match;
    values[name] = value.trim();
  }
  return values;
}

function assertSanity(light, dark) {
  const required = ["--background", "--foreground", "--primary", "--border", "--radius"];
  const missing = required.filter((name) => !(name in light));
  if (missing.length > 0) {
    throw new Error(
      `design:sync: token values: expected tokens missing from :root parse: ${missing.join(", ")}`
    );
  }
  if (!("--background" in dark) || Object.keys(dark).length < 20) {
    throw new Error(
      "design:sync: token values: .dark parse looks incomplete (missing --background or fewer than 20 tokens)"
    );
  }
}

function toTsRecord(values) {
  return Object.entries(values)
    .map(([name, value]) => `        ${JSON.stringify(name)}: ${JSON.stringify(value)},`)
    .join("\n");
}

export function buildTokenValuesTs({ root = ROOT } = {}) {
  const source = readText(join(root, "src", "globals.css"));
  const light = parseDeclarations(extractBlock(source, ":root"));
  const dark = parseDeclarations(extractBlock(source, ".dark"));
  assertSanity(light, dark);

  return `// AUTO-GENERATED by scripts/design-sync/sync-token-values.mjs — do not edit.
// Static token values from src/globals.css (the token SSOT). Docs pages use
// these so server-rendered HTML carries real values for text-only consumers;
// the browser then keeps them live via getComputedStyle. (#687)

export const TOKEN_VALUES: {
    light: Record<string, string>;
    dark: Record<string, string>;
} = {
    light: {
${toTsRecord(light)}
    },
    dark: {
${toTsRecord(dark)}
    },
};
`;
}

export function syncTokenValues({ root = ROOT } = {}) {
  const outputPath = join(root, "app", "lib", "data", "token-values.generated.ts");
  writeText(outputPath, buildTokenValuesTs({ root }));
  console.log(
    "design:sync: Updated app/lib/data/token-values.generated.ts from src/globals.css (static token values)"
  );
}
