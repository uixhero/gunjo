import { join } from "node:path";
import { ROOT, readJson, writeText } from "./shared.mjs";
import {
  COMPONENT_SOURCE_CATEGORY_CONFIGS,
  getComponentFileName,
} from "./component-source-map.mjs";

const CATEGORY_LABELS = {
  patterns: "Patterns",
  feedback: "Feedback",
  layout: "Layout",
  navigation: "Navigation",
  overlay: "Overlay",
  display: "Display",
  inputs: "Inputs",
};

const EXTRA_EXPORTS_BY_CATEGORY = {
  patterns: [],
  feedback: [],
  layout: [],
  navigation: [],
  overlay: ["AIChatInput", "AIChatMessage"],
  display: [],
  inputs: ["ButtonVariants"],
};

const EXTRA_PUBLIC_EXPORT_SECTIONS = [
  {
    label: "Chart Types",
    exports: [
      "export type { ChartColor, ChartDataPoint, ChartTone } from './components/display/chart-utils';",
    ],
  },
  {
    label: "Table Types",
    exports: [
      "export type { ColumnDef } from '@tanstack/react-table';",
    ],
  },
];

function withoutTsx(fileName) {
  return fileName.endsWith(".tsx") ? fileName.slice(0, -4) : fileName;
}

function buildCategoryExports({ root, category, specPath, fileNameOverrides }) {
  const spec = readJson(join(root, specPath));
  const componentKeys = Object.keys(spec?.components ?? {}).sort((a, b) => a.localeCompare(b));
  const baseExports = componentKeys.map((componentKey) =>
    withoutTsx(getComponentFileName(componentKey, fileNameOverrides))
  );
  const extras = EXTRA_EXPORTS_BY_CATEGORY[category] ?? [];
  const names = [...baseExports, ...extras];
  const deduped = [...new Set(names)];

  return deduped.map((name) => `export * from './components/${category}/${name}';`);
}

function renderIndexSource(sections) {
  const lines = [
    "/**",
    " * @gunjo/ui – Design system component library.",
    " * Generated by `npm run design:sync:components`. Do not edit manually.",
    " */",
    "",
  ];

  for (const section of sections) {
    lines.push(`/** ${section.label} */`);
    lines.push(...section.exports);
    lines.push("");
  }

  for (const section of EXTRA_PUBLIC_EXPORT_SECTIONS) {
    lines.push(`/** ${section.label} */`);
    lines.push(...section.exports);
    lines.push("");
  }

  lines.push("/** Utility */");
  // Hand-authored runtime primitives with no design node (no `.pen`): theme
  // coordination lives here alongside `cn`, not in a spec'd category. (#171)
  lines.push("export * from './components/utility/ThemeProvider';");
  // Number / currency / percent formatters (JPY-first) — fintech/commerce/
  // analytics screens hand-rolled these for lack of a shipped helper. (#180)
  lines.push("export { formatCurrency, formatNumber, formatPercent } from './lib/format';");
  lines.push("export type { FormatCurrencyOptions, FormatNumberOptions, FormatPercentOptions } from './lib/format';");
  lines.push("export { cn } from './lib/utils';");
  lines.push("export { componentManifest } from './components/generated/component-manifest';");
  lines.push("export { componentStyleHints } from './components/generated/component-style-hints';");

  return `${lines.join("\n")}\n`;
}

export function syncPublicExports({ root = ROOT } = {}) {
  const sections = COMPONENT_SOURCE_CATEGORY_CONFIGS.map((config) => ({
    label: CATEGORY_LABELS[config.category] ?? config.category,
    exports: buildCategoryExports({ root, ...config }),
  }));

  const outputPath = join(root, "src", "index.ts");
  writeText(outputPath, renderIndexSource(sections));
  console.log("design:sync: Updated src/index.ts from component-specs");
}
